					<h3 class="service-detail_subheading">Steps for Patent Registration in India</h3>


					<p>Acquiring a patent registration in India involves a systematic procedure, commencing with a Patent Search and culminating in issuing a Patent Registration Certificate.</p>
					<p>Here's a breakdown of the patent filing process in India:</p>
				</div>
			
			</div>


			
			<div class="row clearfix">
				<!-- Column -->
				<div class="column col-lg-6 col-md-12 col-sm-12">
					<h6 class="service-detail_subheading">Step 1: Conducting Patent Search</h6>


					<p>Begin the process with a thorough patent search to ascertain the invention's uniqueness. It's advisable to undertake patent searches before filing to gauge the novelty of your invention. If your invention mirrors prior art or closely resembles existing patents, its novelty might be contested by the Indian Patent Office. Hence, preliminary patent searches are pivotal to gauging the probability of patent approval.</p>
				</div>

				<!-- Column -->
				<div class="column col-lg-6 col-md-12 col-sm-12">
					<h6 class="service-detail_subheading">Step 2: Drafting the Patent Specification</h6>


					<p>Upon completing the international searches, the next step is to draft the patent specification. This document, framed in technical and legal terms, may or may not incorporate the inventor's claims. Absence of claims indicates a provisional specification; presence denotes a complete one. The specification outlines the invention, offering a comprehensive description, practical instances, and the best implementation method. A patent is legally shielded when its specification, including the inventor's claims, is exhaustive.</p>
				</div>
			



					<!-- Column -->
					<div class="column col-lg-6 col-md-12 col-sm-12">
					<h6 class="service-detail_subheading">Step 3: Filing the Patent Application</h6>


					<p>After preparing the Patent Specification, you can proceed with the patent application filing in India. Depending on the drafted specifications, applications can be either provisional or complete. The Provisional or Complete Specification is lodged in Form 2, whereas the Patent Application is in Form 1, in line with the Indian Patent Act. If you've filed a provisional patent specification, a complete one encompassing the inventor's claims should be submitted within 12 months. Ensure to pay the requisite patent registration fees along with the application.</p>
				</div>

				<!-- Column -->
				<div class="column col-lg-6 col-md-12 col-sm-12">
					<h6 class="service-detail_subheading">Step 4: Publicizing Patent for Opposition</h6>


					<p>Post the patent application, the patent is listed in an official gazette for public scrutiny. This transparency enables the public to raise objections against the patent, provided they have valid reasons.</p>
				</div>






						<!-- Column -->
						<div class="column col-lg-6 col-md-12 col-sm-12">
					<h6 class="service-detail_subheading">Step 5: Initiating Patent Examination</h6>


					<p>The patent application undergoes examination only after an explicit examination request has been made. This request should be made within 48 months from the patent's filing or its priority date. An assigned patent examiner then scrutinizes the application, releasing an examination report highlighting concerns. A response to this report must be dispatched within a year of its release. If needed, the examiner might summon the applicant or representative to a hearing to address these concerns, termed patent prosecution.</p>
				</div>

		
			</div>

		



