					<h3 class="service-detail_subheading">International trademark registration procedure</h3>


					<p>The international trademark registration process primarily utilises the Madrid System, offering a streamlined and cost-effective approach for registering and managing trademarks globally. Here is a detailed overview of the international trademark registration procedure:</p>



					<ul class="service-detail_list my-5">
						<li><i class="fa-solid fa-circle-check fa-fw"></i><span><strong>Global Trademark Search:  </strong>Before applying for international trademark registration, the applicant must perform a global trademark search. This can be done using the WIPO’s Global Brand Database to check for similar or identical trademarks that are already registered. It's essential to ensure that no conflicting trademarks exist in the Madrid Protocol countries where the trademark registration is intended. If a conflicting trademark is found, the application may be rejected.</span></li>

						<li><i class="fa-solid fa-circle-check fa-fw"></i><span>Basic Application or Registration: You must first have an existing trademark application or registration (the basic mark) in your home country's trademark office. This acts as the foundation for your international application.</span></li>
						


						<li><i class="fa-solid fa-circle-check fa-fw"></i><span>International Application: You can file a single international application through the World Intellectual Property Organization (WIPO). This application allows you to choose the member countries of the Madrid System where you wish to protect your trademark. Depending on local regulations, you can submit this application directly to WIPO or through your national or regional trademark office.</span></li>


						<li><i class="fa-solid fa-circle-check fa-fw"></i><span>Designation of Countries: In the international application, specify the Madrid System member countries where you seek trademark protection. These designations indicate where you plan to use your trademark actively.</span></li>

						<li><i class="fa-solid fa-circle-check fa-fw"></i><span>Payment of Fees: The fees for international registration include a basic fee and additional fees for each country you designate. These fees may vary depending on whether your trademark application is for a black-and-white or colour mark and how many classes of goods or services you are registering.</span></li>

						<li><i class="fa-solid fa-circle-check fa-fw"></i><span>Examination by WIPO: WIPO reviews your application for compliance with the Madrid System's requirements. If your application meets all criteria, WIPO will record your trademark in the International Register and publish it in the WIPO Gazette of International Marks.</span></li>

						<li><i class="fa-solid fa-circle-check fa-fw"></i><span>Examination by Designated Countries: Following approval from WIPO, each designated country will conduct its review based on local trademark laws. This may include checks for conflicts with existing trademarks and adherence to local regulations.</span></li>

						<li><i class="fa-solid fa-circle-check fa-fw"></i><span>Notification of Decisions: WIPO will inform you of the decisions from each designated country's trademark office. If any country refuses protection, you can address these refusals directly with the respective country's office, typically requiring the involvement of local agents or attorneys.</span></li>

						<li><i class="fa-solid fa-circle-check fa-fw"></i><span>Protection and Renewal: If granted, your international registration protects your trademark in the designated countries for 10 years from the registration date. This protection can be renewed for subsequent 10-year periods. You can manage your registration through WIPO, including handling renewals, adding new country designations, and changing your registration.</span></li>

						<li><i class="fa-solid fa-circle-check fa-fw"></i><span>Management of the International Registration: You may need to record changes with WIPO throughout the life of the international registration, such as changes in ownership or updates to the holder's name or address.</span></li>
						<li><i class="fa-solid fa-circle-check fa-fw"></i><span>Use Requirements: In many jurisdictions, active use of the trademark is necessary to maintain registration. In some countries, you may need to provide proof of use to prevent cancellation.</span></li>


						
					</ul>

